fun main()
{
    val a: Int = 10
    val b: Double = a.toDouble()
    val c: String = "20"
    val d: Int = c.toInt()
    val e: Double = c.toDouble()

    println("Integer Value:-" + a)
    println("Double Value (from Integer) :- " + b)
    println("String Value :- " + c)
    println("Integer Value (from String) :-" + d)
    println("Double Value (from String) :-" + e)
}


